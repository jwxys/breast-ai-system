## 3. 方剂推荐系统 (TCM-PKG)

### 基本信息
- **系统名称**: TCM Prescription Knowledge Graph
- **数据文件**: `models/tcm_knowledge_graph.json`
- **数据大小**: 2.4 MB
- **类型**: 知识图谱 + 规则引擎

### 知识来源
| 来源类型 | 具体来源 | 条目数 |
|---------|----------|--------|
| 经典著作 | 《中医妇科学》《中医外科学》等 | 12 部 |
| 指南共识 | 《乳腺癌中西医结合诊疗指南》 | 3 部 |
| 临床经验 | 本院名老中医经验方 | 48 首 |
| 现代研究 | CNKI 文献 Meta 分析 | 156 篇 |

### 方剂库规模
- **基础方剂**: 36 首
- **加减化裁**: 280 种变化
- **药对配伍**: 156 对
- **禁忌提醒**: 89 条

### 推荐算法
```
推荐分数 = 证型匹配度 × 0.5 
         + 症状匹配度 × 0.3 
         + 体质适配度 × 0.2
         - 禁忌扣分
```

### 示例：肝郁气滞证推荐
- **主方**: 逍遥散 (柴胡、当归、白芍、白术、茯苓、甘草)
- **加减**:
  - 乳房胀痛明显：加青皮、香附
  - 情绪抑郁：加合欢皮、郁金
  - 月经不调：加益母草、川芎

